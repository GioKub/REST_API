const mongoose = require('mongoose');

const subscriberSchema = new mongoose.Schema({
  nickname: {
    type: String,
    required: true,
  },
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  salt: {
    type: String,
  },
});

module.exports = mongoose.model('Student', subscriberSchema);
